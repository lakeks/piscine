#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 != '\0' && *s1 != '\0')
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		else
			break ;
	}
	if (*s1 == '\0' && *s2 == '\0')
		return (0);
	return (*(unsigned char *)(s1) - *(unsigned char *)(s2));
}
